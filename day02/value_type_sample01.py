# 整数和浮点数示例
a = 10        # 正整数
b = -3        # 负整数
c = 0         # 零
d = 3.14      # 浮点数
e = -0.001    # 负浮点数
f = 2 + 3j    # 复数
print("整数和浮点数:")
print(f"a: {a}, 类型: {type(a)}") 
print(f"b: {b}, 类型: {type(b)}")
print(f"c: {c}, 类型: {type(c)}")
print(f"d: {d}, 类型: {type(d)}")
print(f"e: {e}, 类型: {type(e)}")
print(f"f: {f}, 类型: {type(f)}")
print("\n布尔类型:")
is_active = True # 布尔值True
is_closed = False # 布尔值False
print(f"is_active: {is_active}, 类型: {type(is_active)}")
print(f"is_closed: {is_closed}, 类型: {type(is_closed)}")
print("\nNone类型:")
result = None # None类型，表示无值